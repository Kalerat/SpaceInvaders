Obstacle::Obstacle(Vector2 position, Color color)
    : position(position), color(color) {

        // Create blocks based on the grid
        for (size_t i = 0; i < grid.size(); ++i) {
            for (size_t j = 0; j < grid[i].size(); ++j) {
                if (grid[i][j] == 1) {
                    Vector2 blockPosition = { position.x + j * BLOCK_SIZE, position.y + i * BLOCK_SIZE };
                    blocks.emplace_back(blockPosition, color);
                }
            }
        }
}


void Obstacle::Draw() {
    for (auto& block : blocks) {
        block.Draw(); // Draw each block in the obstacle
    }
}

